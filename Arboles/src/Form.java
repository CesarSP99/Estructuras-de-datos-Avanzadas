    private void jBTieneNietosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTieneNietosActionPerformed
        int x = Integer.parseInt(JOptionPane.showInputDialog("Ingrese hijo para pillar si tiene nietos o no:"));
        if(arbol.existe(x))
            JOptionPane.showMessageDialog(rootPane, arbol.tieneNietos(x));
        else
            JOptionPane.showMessageDialog(rootPane, "No existe un nodo con ese valor");
    }//GEN-LAST:event_jBTieneNietosActionPerformed

