    private void jBDevolverAbueloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDevolverAbueloActionPerformed
        int abuelo = arbol.retornaAbuelo(Integer.parseInt(JOptionPane.showInputDialog("Ingrese hijo para pillar abuelo:")));
        if( abuelo != 0)
            JOptionPane.showMessageDialog(rootPane, "Su abuelo es " + abuelo);
    }//GEN-LAST:event_jBDevolverAbueloActionPerformed

