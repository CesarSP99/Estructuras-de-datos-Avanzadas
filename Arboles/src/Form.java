    private void jBExisteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExisteActionPerformed
        int num;
        num = Integer.parseInt(JOptionPane.showInputDialog("Ingrese numero a buscar"));
        if(arbol.existe(num))
            JOptionPane.showMessageDialog(rootPane, "Existe");
        else
            JOptionPane.showMessageDialog(rootPane, "No existe");
    }//GEN-LAST:event_jBExisteActionPerformed

    private void jBHermanosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBHermanosActionPerformed
        int x;
        x = Integer.parseInt(JOptionPane.showInputDialog("Ingrese primer numero"));
        int y;
        y = Integer.parseInt(JOptionPane.showInputDialog("Ingrese segundo numero"));
        if(arbol.brother(x, y))
            JOptionPane.showMessageDialog(rootPane, "Son hermanos");
        else
            JOptionPane.showMessageDialog(rootPane, "No son hermanos");
    }//GEN-LAST:event_jBHermanosActionPerformed

