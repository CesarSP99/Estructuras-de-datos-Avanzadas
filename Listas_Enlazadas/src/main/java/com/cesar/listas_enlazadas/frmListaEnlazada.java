    private void btnLeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLeerActionPerformed
        int n = Integer.parseInt(txtCantidad.getText());
        int i = 1;
        while(i <= n){
            int x = Integer.parseInt(JOptionPane.showInputDialog("Elemento"));
            lista1.insertarUltimo(x);
            i++;
        }
    }//GEN-LAST:event_btnLeerActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
        jtaDatos.setText(null);
        Nodo nodo = lista1.Primero();
        while(nodo != null){
            jtaDatos.append(String.valueOf(nodo.getElem())+ " ");
            nodo = nodo.getProx();
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

